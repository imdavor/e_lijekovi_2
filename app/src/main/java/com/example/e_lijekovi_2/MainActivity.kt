@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun PocetniEkran(context: Context? = null) {
    val lijekovi = remember { mutableStateListOf<Lijek>() }
    var idCounter by rememberSaveable { mutableIntStateOf(0) }
    var editLijek by remember { mutableStateOf<Lijek?>(null) }
    var showAddLijek by remember { mutableStateOf(false) }
    var showExportImportDialog by remember { mutableStateOf(false) }
    var showMessage by remember { mutableStateOf<String?>(null) }
    var currentScreen by remember { mutableStateOf("home") }
    val drawerState = rememberDrawerState(initialValue = DrawerValue.Closed)
    val scope = rememberCoroutineScope()
    val snackbarHostState = remember { SnackbarHostState() }

    val saveData = {
        context?.let { ctx ->
            LijekoviDataManager.saveToLocalStorage(ctx, lijekovi)
        }
        Unit
    }

    // snapshot key to force recomputation of derived aggregates when list items change
    var priceSnapshot by remember { mutableStateOf(0L) }
    fun touchSnapshot() { priceSnapshot = priceSnapshot + 1 }

    // Derived total for reorder computed at owner level so it tracks list changes reliably
    val reorderTotal by derivedStateOf {
        // read priceSnapshot to make derivedStateOf observe it (so touchSnapshot() forces recompute)
        run { priceSnapshot }

        var totalCost = 0.0
        for (l in lijekovi) {
            // parse unitsPerDose from `doza` (first integer found), default 1
            val unitsPerDose = l.doza.trim().let { d -> Regex("\\d+").find(d)?.value?.toIntOrNull() ?: 1 }

            // determine doses per day
            val dosesPerDay = when (l.tipUzimanja) {
                TipUzimanja.STANDARDNO -> {
                    val stdTimes = listOf(l.jutro, l.popodne, l.vecer).count { it }
                    if (stdTimes > 0) stdTimes.toDouble() else 1.0
                }
                TipUzimanja.INTERVALNO -> {
                    val interval = l.intervalnoUzimanje?.intervalSati ?: 0
                    if (interval > 0) 24.0 / interval else 1.0
                }
            }

            val dailyConsumption = unitsPerDose * dosesPerDay
            val daysRemaining = if (dailyConsumption > 0.0) l.trenutnoStanje.toDouble() / dailyConsumption else Double.POSITIVE_INFINITY

            val isYellow = (l.trenutnoStanje <= 7) || (unitsPerDose >= 2 && l.trenutnoStanje <= 14) || (daysRemaining <= 7.0)

            if (isYellow) {
                val price = l.cijena.replace(',', '.').toDoubleOrNull() ?: 0.0
                if (price > 0.0) totalCost += price
            }
        }
        totalCost
    }

    // Enhanced add with highlight animation
    val handleAddLijek: (Lijek) -> Unit = { newLijek ->
        val lijekWithId = newLijek.copy(id = idCounter++)
        // If a price was provided, create initial history entry
        val nowStamp = IntervalnoUzimanje.createDateTimeFormat().format(java.util.Date())
        val withHistory = if (lijekWithId.cijena.isNotBlank()) {
            lijekWithId.copy(cijenaHistorija = lijekWithId.cijenaHistorija + PriceEntry(timestamp = nowStamp, price = lijekWithId.cijena))
        } else lijekWithId

        lijekovi.add(withHistory)
        saveData()
        touchSnapshot()
        showAddLijek = false

        scope.launch {
            snackbarHostState.showSnackbar(
                message = "Terapija '${newLijek.naziv}' dodana",
                actionLabel = "Uredi",
                duration = SnackbarDuration.Short
            ).also { result ->
                if (result == SnackbarResult.ActionPerformed) {
                    editLijek = withHistory
                }
            }

            // Clear highlight after animation
            kotlinx.coroutines.delay(1000)
        }
    }

    LaunchedEffect(Unit) {
        context?.let { ctx ->
            val loadedLijekovi = LijekoviDataManager.loadFromLocalStorage(ctx)
            if (loadedLijekovi != null && loadedLijekovi.isNotEmpty()) {
                lijekovi.clear()
                lijekovi.addAll(loadedLijekovi)
                idCounter = (loadedLijekovi.maxOfOrNull { it.id } ?: -1) + 1
                touchSnapshot()
            }
        }
    }

    val exportLauncher = rememberLauncherForActivityResult(
        contract = ActivityResultContracts.CreateDocument("application/json")
    ) { uri ->
        uri?.let {
            context?.let { ctx ->
                val success = LijekoviDataManager.saveToFile(ctx, it, lijekovi)
                showMessage = if (success) "Podaci uspje≈°no eksportirani!" else "Gre≈°ka pri exportu!"
            }
        }
    }

    val importLauncher = rememberLauncherForActivityResult(
        contract = ActivityResultContracts.OpenDocument()
    ) { uri ->
        uri?.let {
            context?.let { ctx ->
                try {
                    val importedLijekovi = LijekoviDataManager.loadFromFile(ctx, it)
                    if (importedLijekovi != null) {
                        lijekovi.clear()
                        lijekovi.addAll(importedLijekovi)
                        idCounter = (importedLijekovi.maxOfOrNull { lijek -> lijek.id } ?: -1) + 1
                        saveData()
                        touchSnapshot()
                        currentScreen = "home"
                        showMessage = "‚úÖ Podaci uspje≈°no importirani!\n\nUƒçitano ${importedLijekovi.size} lijekova."
                    } else {
                        showMessage = """
                            ‚ùå Gre≈°ka pri importu podataka!
                            
                            Moguƒái uzroci:
                            ‚Ä¢ Datoteka nije valjani JSON format
                            ‚Ä¢ JSON ne odgovara strukturi aplikacije  
                            ‚Ä¢ Datoteka je o≈°teƒáena ili prazna
                            ‚Ä¢ Nema dozvolu za ƒçitanje datoteke
                            
                            üí° Savjet: Poku≈°ajte exportirati podatke iz aplikacije pa ih importirati nazad da testirate format.
                            
                            üîç Za detaljne informacije provjerite Logcat (filtriraj: LijekoviDataManager)
                        """.trimIndent()
                    }
                } catch (e: Exception) {
                    showMessage = """
                        ‚ùå Neoƒçekivana gre≈°ka pri importu!
                        
                        Gre≈°ka: ${e.message ?: "Nepoznata gre≈°ka"}
                        
                        üí° Provjerite:
                        ‚Ä¢ Je li datoteka ispravno eksportirana iz aplikacije
                        ‚Ä¢ Imate li dozvolu za ƒçitanje datoteke
                        ‚Ä¢ Nije li datoteka o≈°teƒáena
                    """.trimIndent()
                }
            }
        }
    }

    showMessage?.let { message ->
        AlertDialog(
            onDismissRequest = { showMessage = null },
            title = { Text("Obavijest") },
            text = { Text(message) },
            confirmButton = {
                TextButton(onClick = { showMessage = null }) {
                    Text("U redu")
                }
            }
        )
    }

    if (showExportImportDialog) {
        AlertDialog(
            onDismissRequest = { showExportImportDialog = false },
            title = { Text("Upravljanje podacima") },
            text = {
                Column(verticalArrangement = Arrangement.spacedBy(8.dp)) {
                    Text("Odaberite akciju:")
                }
            },
            confirmButton = {
                Column(verticalArrangement = Arrangement.spacedBy(8.dp)) {
                    Button(
                        onClick = {
                            showExportImportDialog = false
                            exportLauncher.launch("lijekovi_backup.json")
                        },
                        modifier = Modifier.fillMaxWidth()
                    ) {
                        Text("Exportaj podatke")
                    }
                    Button(
                        onClick = {
                            showExportImportDialog = false
                            importLauncher.launch(arrayOf("application/json"))
                        },
                        modifier = Modifier.fillMaxWidth()
                    ) {
                        Text("Importaj podatke")
                    }
                    Button(
                        onClick = {
                            lijekovi.clear()
                            saveData()
                            showExportImportDialog = false
                            showMessage = "Svi podaci su uspje≈°no obrisani!"
                        },
                        modifier = Modifier.fillMaxWidth(),
                        colors = ButtonDefaults.buttonColors(containerColor = Color.Red)
                    ) {
                        Text("Obri≈°i sve podatke", color = Color.White)
                    }
                }
            },
            dismissButton = {
                TextButton(onClick = { showExportImportDialog = false }) {
                Text("Odustani")
            }
        })
    }

    ModalNavigationDrawer(
        drawerState = drawerState,
        drawerContent = {
            ModalDrawerSheet {
                Column(
                    modifier = Modifier
                        .fillMaxSize()
                        .padding(16.dp)
                ) {
                    Text(
                        text = "e-LijekoviHR",
                        style = MaterialTheme.typography.headlineMedium,
                        fontWeight = FontWeight.Bold,
                        color = MaterialTheme.colorScheme.primary,
                        modifier = Modifier.padding(vertical = 16.dp)
                    )

                    Divider(modifier = Modifier.padding(bottom = 8.dp))

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Home, contentDescription = "Poƒçetna") },
                        label = { Text("Poƒçetna") },
                        selected = currentScreen == "home",
                        onClick = {
                            currentScreen = "home"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Analytics, contentDescription = "Statistike") },
                        label = { Text("Statistike") },
                        selected = currentScreen == "statistics",
                        onClick = {
                            currentScreen = "statistics"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Settings, contentDescription = "Postavke") },
                        label = { Text("Postavke") },
                        selected = currentScreen == "settings",
                        onClick = {
                            currentScreen = "settings"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Info, contentDescription = "O aplikaciji") },
                        label = { Text("O aplikaciji") },
                        selected = currentScreen == "about",
                        onClick = {
                            currentScreen = "about"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )
                }
            }
        }
    ) {
        Scaffold(
            topBar = {
                TopAppBar(
                    title = {
                        Text(
                            when (currentScreen) {
                                "home" -> "Moji lijekovi"
                                "statistics" -> "Statistike"
                                "settings" -> "Postavke"
                                "about" -> "O aplikaciji"
                                else -> "e-LijekoviHR"
                            }
                        )
                    },
                    navigationIcon = {
                        IconButton(
                            onClick = {
                                scope.launch { drawerState.open() }
                            }
                        ) {
                            Icon(Icons.Default.Menu, contentDescription = "Menu")
                        }
                    }
                )
            },
            floatingActionButton = {
                if (currentScreen == "home") {
                    AnimatedFAB(
                        isExpanded = showAddLijek,
                        onClick = { showAddLijek = true }
                    )
                }
            },
            snackbarHost = {
                SnackbarHost(hostState = snackbarHostState)
            }
        ) { paddingValues ->
            when (currentScreen) {
                "home" -> {
                    HomeScreen(
                        lijekovi = lijekovi,
                        reorderTotal = reorderTotal,
                        onTake = { lijek, dobaDana ->
                            val grupa = when (dobaDana) {
                                DobaDana.JUTRO -> DobaDana.JUTRO
                                DobaDana.POPODNE -> DobaDana.POPODNE
                                DobaDana.VECER -> DobaDana.VECER
                                else -> null
                            }
                            if (grupa != null && lijek.mozeUzeti(grupa)) {
                                val idx = lijekovi.indexOf(lijek)
                                if (idx != -1) {
                                    // Save previous for undo
                                    val previous = lijekovi[idx]
                                    val updated = previous.uzmiLijek(grupa)
                                    lijekovi[idx] = updated
                                    saveData()
                                    touchSnapshot()

                                    scope.launch {
                                        val result = snackbarHostState.showSnackbar(
                                            message = "Uspje≈°no uzeto: ${lijek.naziv}",
                                            actionLabel = "Poni≈°ti",
                                            duration = SnackbarDuration.Short
                                        )

                                        if (result == SnackbarResult.ActionPerformed) {
                                            // restore previous state
                                            val restoreIdx = lijekovi.indexOfFirst { it.id == previous.id }
                                            if (restoreIdx != -1) {
                                                lijekovi[restoreIdx] = previous
                                                saveData()
                                                touchSnapshot()
                                            }
                                        }
                                    }
                                }

                                // Notify user that therapy was taken (use available context and lijek name)
                                context?.let { ctx ->
                                    NotificationScheduler.sendTherapyTakenNotification(ctx, lijek.naziv)
                                }
                            } else {
                                scope.launch {
                                    snackbarHostState.showSnackbar(
                                        "Nije moguƒáe uzeti dozu za ${lijek.naziv}",
                                        duration = SnackbarDuration.Short
                                    )
                                }
                            }
                        },
                        onEdit = { lijek -> editLijek = lijek },
                        modifier = Modifier.padding(paddingValues))
                }
                "statistics" -> {
                    StatisticsScreen(
                        lijekovi = lijekovi,
                        modifier = Modifier.padding(paddingValues)
                    )
                }
                "settings" -> {
                    SettingsScreen(
                        onExportImport = { showExportImportDialog = true },
                        modifier = Modifier.padding(paddingValues)
                    )
                }
                "about" -> {
                    AboutScreen(modifier = Modifier.padding(paddingValues))
                }
            }
        }
    }

    if (showAddLijek) {
        LijekDialog(
            lijek = null,
            onDismiss = { showAddLijek = false },
            onSave = { newLijek ->
                 val duplicate = lijekovi.any {
                     it.naziv.lowercase() == newLijek.naziv.lowercase()
                 }

                 if (duplicate) {
                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${newLijek.naziv}' veƒá postoji!",
                             duration = SnackbarDuration.Short
                         )
                     }
                     return@LijekDialog
                 }

                 handleAddLijek(newLijek)
             }
            , onDelete = { /* no-op for add dialog */ }
        )
     }

     editLijek?.let { lijek ->
         LijekDialog(
             lijek = lijek,
             onDismiss = { editLijek = null },
             onSave = { updatedLijek ->
                 val duplicate = lijekovi.any {
                     it.id != lijek.id && it.naziv.lowercase() == updatedLijek.naziv.lowercase()
                 }

                 if (duplicate) {
                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${updatedLijek.naziv}' veƒá postoji!",
                             duration = SnackbarDuration.Short
                         )
                     }
                     return@LijekDialog
                 }

                 val index = lijekovi.indexOfFirst { it.id == lijek.id }
                 if (index != -1) {
                     // When editing, if price changed, append to cijenaHistorija
                     val prev = lijekovi[index]
                     var updatedWithHistory = updatedLijek
                     if (prev.cijena != updatedLijek.cijena) {
                         val nowStamp = IntervalnoUzimanje.createDateTimeFormat().format(java.util.Date())
                         updatedWithHistory = if (updatedLijek.cijena.isNotBlank()) {
                            updatedLijek.copy(cijenaHistorija = prev.cijenaHistorija + PriceEntry(timestamp = nowStamp, price = updatedLijek.cijena))
                        } else {
                            // If price cleared, don't append empty entry; just preserve history and clear cijena field
                            updatedLijek.copy(cijenaHistorija = prev.cijenaHistorija)
                        }
                     } else {
                         // preserve previous history if unchanged
                         updatedWithHistory = updatedLijek.copy(cijenaHistorija = prev.cijenaHistorija)
                     }

                     lijekovi[index] = updatedWithHistory
                     saveData()
                     touchSnapshot()

                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${updatedLijek.naziv}' a≈æuriran",
                             duration = SnackbarDuration.Short
                         )
                     }
                 }
                 editLijek = null
             },
             onDelete = { deletedLijek ->
                 val idx = lijekovi.indexOfFirst { it.id == deletedLijek.id }
                 if (idx != -1) {
                     // Remove and keep a reference to the removed item so we can restore it on Undo
                     val removed = lijekovi.removeAt(idx)
                     saveData()
                     touchSnapshot()
                     scope.launch {
                         // Show snackbar with 'Poni≈°ti' action to allow undoing the delete
                         val result = snackbarHostState.showSnackbar(
                             message = "Lijek '${removed.naziv}' obrisan",
                             actionLabel = "Poni≈°ti",
                             duration = SnackbarDuration.Short
                         )

                         if (result == SnackbarResult.ActionPerformed) {
                             // Reinsert the removed item at its previous index (or end if index out of range)
                             val insertIndex = if (idx <= lijekovi.size) idx else lijekovi.size
                             lijekovi.add(insertIndex, removed)
                             saveData()
                             touchSnapshot()
                         }
                     }
                 }
                 editLijek = null
             }
         )
     }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun AnimatedFAB(
    isExpanded: Boolean,
    onClick: () -> Unit,
    modifier: Modifier = Modifier
) {
    val rotation by animateFloatAsState(
        targetValue = if (isExpanded) 45f else 0f,
        animationSpec = spring(
            dampingRatio = Spring.DampingRatioMediumBouncy,
            stiffness = Spring.StiffnessMedium
        ),
        label = "fab_rotation"
    )

    FloatingActionButton(
        onClick = onClick,
        modifier = modifier,
        containerColor = MaterialTheme.colorScheme.primary
    ) {
        Icon(
            imageVector = Icons.Default.Add,
            contentDescription = "Dodaj lijek",
            modifier = Modifier.graphicsLayer(rotationZ = rotation)
        )
    }
}

@Composable
fun HomeScreen(
    lijekovi: List<Lijek>,
    reorderTotal: Double,
    onTake: (Lijek, DobaDana?) -> Unit,
    onEdit: (Lijek) -> Unit,
    modifier: Modifier = Modifier,
    scaffoldPadding: PaddingValues = PaddingValues(0.dp)
) {
    val localContext = LocalContext.current


    val grupe = listOf(
        DobaDana.JUTRO to "Jutro",
        DobaDana.POPODNE to "Podne",
        DobaDana.VECER to "Veƒçer"
    )
    val skipSnackbarOnTakeAll = remember { mutableStateOf(false) }
    LazyColumn(
        modifier = modifier
            .fillMaxSize()
            .padding(16.dp),
        verticalArrangement = Arrangement.spacedBy(18.dp),
        contentPadding = PaddingValues(
            bottom = scaffoldPadding.calculateBottomPadding()
                + WindowInsets.navigationBars.asPaddingValues().calculateBottomPadding()
                + 16.dp // internal margin
        )
    ) {
        // show reorder summary as first item
        item {
            Card(
                modifier = Modifier
                    .fillMaxWidth()
                    .shadow(elevation = 2.dp, shape = RoundedCornerShape(12.dp))
                    .clip(RoundedCornerShape(12.dp)),
                elevation = CardDefaults.cardElevation(defaultElevation = 0.dp)
            ) {
                Column(modifier = Modifier.padding(12.dp)) {
                    val fmt = { v: Double -> String.format(java.util.Locale.getDefault(), "%.2f ‚Ç¨", v).replace('.', ',') }
                    Text("üõí Ukupno za narud≈æbu: ${fmt(reorderTotal)}", fontWeight = FontWeight.Bold)
                }
            }
        }

        grupe.forEach { (doba, naziv) ->
            val grupaLijekova = lijekovi.filter {
                when (doba) {
                    DobaDana.JUTRO -> it.jutro
                    DobaDana.POPODNE -> it.popodne
                    DobaDana.VECER -> it.vecer
                }
            }
            if (grupaLijekova.isNotEmpty()) {
                item {
                    Row(
                        verticalAlignment = Alignment.CenterVertically,
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(vertical = 10.dp)
                    ) {
                        val ikona = when (doba) {
                            DobaDana.JUTRO -> Icons.Default.WbSunny
                            DobaDana.POPODNE -> Icons.Default.WbTwilight
                            DobaDana.VECER -> Icons.Default.NightsStay
                        }
                        Icon(
                            imageVector = ikona,
                            contentDescription = naziv,
                            tint = MaterialTheme.colorScheme.primary,
                            modifier = Modifier.size(28.dp).padding(end = 10.dp)
                        )
                        Text(
                            text = naziv,
                            style = MaterialTheme.typography.titleLarge,
                            color = MaterialTheme.colorScheme.primary,
                            modifier = Modifier.padding(end = 12.dp)
                        )
                        Spacer(modifier = Modifier.weight(1f))
                        val mozeUzetiNetko = grupaLijekova.any { it.mozeUzeti(doba) && !it.jeUzetZaDanas() && it.trenutnoStanje > 0 }
                        Button(
                            onClick = {
                                skipSnackbarOnTakeAll.value = true
                                var takenCount = 0
                                grupaLijekova.forEach {
                                    if (it.mozeUzeti(doba) && !it.jeUzetZaDanas() && it.trenutnoStanje > 0) {
                                        onTake(it, doba)
                                        takenCount++
                                    }
                                }
                                skipSnackbarOnTakeAll.value = false

                                if (takenCount > 0) {
                                    NotificationScheduler.sendTherapyTakenNotification(localContext, naziv)
                                }
                            },
                            enabled = mozeUzetiNetko,
                            modifier = Modifier
                                .height(38.dp)
                                .padding(vertical = 2.dp)
                        ) {
                            Text("Uzmi sve", fontSize = 15.sp)
                        }
                    }
                }
                items(grupaLijekova.size) { idx ->
                    val lijek = grupaLijekova[idx]
                    Box(modifier = Modifier.clickable { onEdit(lijek) }) {
                        LijekCard(
                            lijek = lijek,
                            onTake = {
                                if (!skipSnackbarOnTakeAll.value) onTake(lijek, doba)
                            },
                            modifier = Modifier
                                .fillMaxWidth()
                                .then(if (idx == grupaLijekova.lastIndex) Modifier.padding(bottom = 16.dp) else Modifier)
                        )
                    }
                }
            }
        }

        val intervalniLijekovi = lijekovi.filter { it.tipUzimanja == TipUzimanja.INTERVALNO }
        if (intervalniLijekovi.isNotEmpty()) {
            item {
                Row(
                    verticalAlignment = Alignment.CenterVertically,
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(vertical = 10.dp)
                ) {
                    Icon(
                        imageVector = Icons.Default.Schedule,
                        contentDescription = "Intervalno",
                        tint = MaterialTheme.colorScheme.primary,
                        modifier = Modifier.size(28.dp).padding(end = 10.dp)
                    )
                    Text(
                        text = "Intervalno",
                        style = MaterialTheme.typography.titleLarge,
                        color = MaterialTheme.colorScheme.primary,
                        modifier = Modifier.padding(end = 12.dp)
                    )
                }
            }
            items(intervalniLijekovi.size) { idx ->
                val lijek = intervalniLijekovi[idx]
                Box(modifier = Modifier.clickable { onEdit(lijek) }) {
                    LijekCard(
                        lijek = lijek,
                        onTake = {
                            if (!skipSnackbarOnTakeAll.value) onTake(lijek, null)
                        },
                        modifier = Modifier.fillMaxWidth()
                    )
                }
            }
        }

        val ostaliLijekovi = lijekovi.filter { !it.jutro && !it.popodne && !it.vecer && it.tipUzimanja != TipUzimanja.INTERVALNO }
        if (ostaliLijekovi.isNotEmpty()) {
            item {
                Row(
                    verticalAlignment = Alignment.CenterVertically,
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(vertical = 10.dp)
                ) {
                    Icon(
                        imageVector = Icons.Default.MedicalServices,
                        contentDescription = "Ostali",
                        tint = MaterialTheme.colorScheme.primary,
                        modifier = Modifier.size(28.dp).padding(end = 10.dp)
                    )
                    Text(
                        text = "Ostali",
                        style = MaterialTheme.typography.titleLarge,
                        color = MaterialTheme.colorScheme.primary,
                        modifier = Modifier.padding(end = 12.dp)
                    )
                }
            }
            items(ostaliLijekovi.size) { idx ->
                val lijek = ostaliLijekovi[idx]
                Box(modifier = Modifier.clickable { onEdit(lijek) }) {
                    LijekCard(
                        lijek = lijek,
                        onTake = {
                            if (!skipSnackbarOnTakeAll.value) onTake(lijek, null)
                        },
                        modifier = Modifier.fillMaxWidth()
                    )
                }
            }
        }
    }
}

