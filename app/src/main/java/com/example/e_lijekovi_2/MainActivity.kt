                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Analytics, contentDescription = "Statistike") },
                        label = { Text("Statistike") },
                        selected = currentScreen == "statistics",
                        onClick = {
                            currentScreen = "statistics"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Settings, contentDescription = "Postavke") },
                        label = { Text("Postavke") },
                        selected = currentScreen == "settings",
                        onClick = {
                            currentScreen = "settings"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )

                    NavigationDrawerItem(
                        icon = { Icon(Icons.Default.Info, contentDescription = "O aplikaciji") },
                        label = { Text("O aplikaciji") },
                        selected = currentScreen == "about",
                        onClick = {
                            currentScreen = "about"
                            scope.launch { drawerState.close() }
                        },
                        modifier = Modifier.padding(NavigationDrawerItemDefaults.ItemPadding)
                    )
                }
            }
        }
    ) {
        Scaffold(
            topBar = {
                TopAppBar(
                    title = {
                        Text(
                            when (currentScreen) {
                                "home" -> "Moji lijekovi"
                                "statistics" -> "Statistike"
                                "settings" -> "Postavke"
                                "about" -> "O aplikaciji"
                                else -> "e-LijekoviHR"
                            }
                        )
                    },
                    navigationIcon = {
                        IconButton(
                            onClick = {
                                scope.launch { drawerState.open() }
                            }
                        ) {
                            Icon(Icons.Default.Menu, contentDescription = "Menu")
                        }
                    }
                )
            },
            floatingActionButton = {
                if (currentScreen == "home") {
                    AnimatedFAB(
                        isExpanded = showAddLijek,
                        onClick = { showAddLijek = true }
                    )
                }
            },
            snackbarHost = {
                SnackbarHost(hostState = snackbarHostState)
            }
        ) { paddingValues ->
            when (currentScreen) {
                "home" -> {
                    HomeScreen(
                        lijekovi = lijekovi,
                        reorderTotal = reorderTotal,
                        onTake = { lijek, dobaDana ->
                            val grupa = when (dobaDana) {
                                DobaDana.JUTRO -> DobaDana.JUTRO
                                DobaDana.POPODNE -> DobaDana.POPODNE
                                DobaDana.VECER -> DobaDana.VECER
                                else -> null
                            }
                            if (grupa != null && lijek.mozeUzeti(grupa)) {
                                val idx = lijekovi.indexOf(lijek)
                                if (idx != -1) {
                                    // Save previous for undo
                                    val previous = lijekovi[idx]
                                    val updated = previous.uzmiLijek(grupa)
                                    lijekovi[idx] = updated
                                    saveData()
                                    touchSnapshot()

                                    scope.launch {
                                        val result = snackbarHostState.showSnackbar(
                                            message = "UspjeÅ¡no uzeto: ${lijek.naziv}",
                                            actionLabel = "PoniÅ¡ti",
                                            duration = SnackbarDuration.Short
                                        )

                                        if (result == SnackbarResult.ActionPerformed) {
                                            // restore previous state
                                            val restoreIdx = lijekovi.indexOfFirst { it.id == previous.id }
                                            if (restoreIdx != -1) {
                                                lijekovi[restoreIdx] = previous
                                                saveData()
                                                touchSnapshot()
                                            }
                                        }
                                    }
                                }

                                // Notify user that therapy was taken (use available context and lijek name)
                                context?.let { ctx ->
                                    NotificationScheduler.sendTherapyTakenNotification(ctx, lijek.naziv)
                                }
                            } else {
                                scope.launch {
                                    snackbarHostState.showSnackbar(
                                        "Nije moguÄ‡e uzeti dozu za ${lijek.naziv}",
                                        duration = SnackbarDuration.Short
                                    )
                                }
                            }
                        },
                        onEdit = { lijek -> editLijek = lijek },
                        modifier = Modifier.padding(paddingValues))
                }
                "statistics" -> {
                    StatisticsScreen(
                        lijekovi = lijekovi,
                        modifier = Modifier.padding(paddingValues)
                    )
                }
                "settings" -> {
                    SettingsScreen(
                        onExportImport = { showExportImportDialog = true },
                        modifier = Modifier.padding(paddingValues)
                    )
                }
                "about" -> {
                    AboutScreen(modifier = Modifier.padding(paddingValues))
                }
            }
        }
    }

    if (showAddLijek) {
        LijekDialog(
            lijek = null,
            onDismiss = { showAddLijek = false },
            onSave = { newLijek ->
                 val duplicate = lijekovi.any {
                     it.naziv.lowercase() == newLijek.naziv.lowercase()
                 }

                 if (duplicate) {
                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${newLijek.naziv}' veÄ‡ postoji!",
                             duration = SnackbarDuration.Short
                         )
                     }
                     return@LijekDialog
                 }

                 handleAddLijek(newLijek)
             }
            , onDelete = { /* no-op for add dialog */ }
        )
     }

     editLijek?.let { lijek ->
         LijekDialog(
             lijek = lijek,
             onDismiss = { editLijek = null },
             onSave = { updatedLijek ->
                 val duplicate = lijekovi.any {
                     it.id != lijek.id && it.naziv.lowercase() == updatedLijek.naziv.lowercase()
                 }

                 if (duplicate) {
                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${updatedLijek.naziv}' veÄ‡ postoji!",
                             duration = SnackbarDuration.Short
                         )
                     }
                     return@LijekDialog
                 }

                 val index = lijekovi.indexOfFirst { it.id == lijek.id }
                 if (index != -1) {
                     lijekovi[index] = updatedLijek
                     saveData()
                     touchSnapshot()

                     scope.launch {
                         snackbarHostState.showSnackbar(
                             message = "Lijek '${updatedLijek.naziv}' aÅ¾uriran",
                             duration = SnackbarDuration.Short
                         )
                     }
                 }
                 editLijek = null
             },
             onDelete = { deletedLijek ->
                 val idx = lijekovi.indexOfFirst { it.id == deletedLijek.id }
                 if (idx != -1) {
                     // Remove and keep a reference to the removed item so we can restore it on Undo
                     val removed = lijekovi.removeAt(idx)
                     saveData()
                     touchSnapshot()
                     scope.launch {
                         // Show snackbar with 'PoniÅ¡ti' action to allow undoing the delete
                         val result = snackbarHostState.showSnackbar(
                             message = "Lijek '${removed.naziv}' obrisan",
                             actionLabel = "PoniÅ¡ti",
                             duration = SnackbarDuration.Short
                         )

                         if (result == SnackbarResult.ActionPerformed) {
                             // Reinsert the removed item at its previous index (or end if index out of range)
                             val insertIndex = if (idx <= lijekovi.size) idx else lijekovi.size
                             lijekovi.add(insertIndex, removed)
                             saveData()
                             touchSnapshot()
                         }
                     }
                 }
                 editLijek = null
             }
         )
     }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun AnimatedFAB(
    isExpanded: Boolean,
    onClick: () -> Unit,
    modifier: Modifier = Modifier
) {
    val rotation by animateFloatAsState(
        targetValue = if (isExpanded) 45f else 0f,
        animationSpec = spring(
            dampingRatio = Spring.DampingRatioMediumBouncy,
            stiffness = Spring.StiffnessMedium
        ),
        label = "fab_rotation"
    )

    FloatingActionButton(
        onClick = onClick,
        modifier = modifier,
        containerColor = MaterialTheme.colorScheme.primary
    ) {
        Icon(
            imageVector = Icons.Default.Add,
            contentDescription = "Dodaj lijek",
            modifier = Modifier.graphicsLayer(rotationZ = rotation)
        )
    }
}

@Composable
fun HomeScreen(
    lijekovi: List<Lijek>,
    reorderTotal: Double,
    onTake: (Lijek, DobaDana?) -> Unit,
    onEdit: (Lijek) -> Unit,
    modifier: Modifier = Modifier,
    scaffoldPadding: PaddingValues = PaddingValues(0.dp)
) {
    val localContext = LocalContext.current


    val grupe = listOf(
        DobaDana.JUTRO to "Jutro",
        DobaDana.POPODNE to "Podne",
        DobaDana.VECER to "VeÄer"
    )
    val skipSnackbarOnTakeAll = remember { mutableStateOf(false) }
    LazyColumn(
        modifier = modifier
            .fillMaxSize()
            .padding(16.dp),
        verticalArrangement = Arrangement.spacedBy(18.dp),
        contentPadding = PaddingValues(
            bottom = scaffoldPadding.calculateBottomPadding()
                + WindowInsets.navigationBars.asPaddingValues().calculateBottomPadding()
                + 16.dp // internal margin
